export const getPhotosByCategory = (category) => {
  if (category === 'all') return galleryPhotos;

  return galleryPhotos.filter(
    photo =>
      photo.category === category &&
      !(category === 'authors' && photo.id === 1) &&
      !(category === 'books' && photo.id === 3)
  );
};

